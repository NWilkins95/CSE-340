/* ****************************************
* Check JWT token and check
* if user has admin/employee privileges.
**************************************** */
validate.checkAdminAccess = (req, res, next) => {
  if (req.cookies.jwt) {
    jwt.verify(
      req.cookies.jwt,
      process.env.ACCESS_TOKEN_SECRET,
      function (err, accountData) {
        if (err) {
          req.flash("notice", "Please log in");
          res.clearCookie("jwt");
          return res.redirect("/account/login");
        }
        if (
          accountData.account_type != "Employee" &&
          accountData.account_type != "Admin"
        ) {
          req.flash("notice", "Access denied. Administrator privileges required.");
          req.flash("notice", "Please login as an administrator.");
          res.clearCookie("jwt");
          return res.redirect("/account/login");
        }
        next();
      }
    );
  } else {
    req.flash("notice", "Please log in");
    return res.redirect("/account/login");
  }
};

/* **********************************
 * Update Personal Info Form Data Validation Rules
 * ********************************* */
validate.updateRules = () => {
  return [
    // firstname is required and must be string
    body("account_firstname")
      .trim()
      .escape()
      .notEmpty()
      .isLength({ min: 1 })
      .withMessage("Please provide a first name."), // on error this message is sent.

    // lastname is required and must be string
    body("account_lastname")
      .trim()
      .escape()
      .notEmpty()
      .isLength({ min: 2 })
      .withMessage("Please provide a last name."), // on error this message is sent.

    // valid email is required and cannot already exist in the DB
    body("account_email")
    .trim()
    .escape()
    .notEmpty()
    .isEmail()
    .normalizeEmail() // refer to validator.js docs
    .withMessage("A valid email is required.")
    .custom(async (account_email, {req}) => {
        const account_id = req.body.account_id
        const emailExists = await accountModel.checkExistingEmail(account_email)
        if (emailExists) {
          const existingAccount = await accountModel.getAccountByEmail(account_email)
          if (String(existingAccount.account_id) !== account_id) {
            throw new Error("Email exists. Please log in or use different email")
          }
        }
      }),
  ]
}

